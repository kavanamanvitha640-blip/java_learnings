class forloopexample {
    public static void main(String[] args) {
        for(int i=2; i<=50; i=i+2) {
            System.out.println(i);
        }
    }
}

class forloopcountdown{
    public static void main(String[] args) {
        for(int i=5; i>=50; i--) {
            System.out.println(i);
        }
    }
}

class dowhileexample{
    public static void main(String[]args) {
        int i=10;

        do{
            System.out.println(i);
            i++;
        }while(i<=5);
    }
}

